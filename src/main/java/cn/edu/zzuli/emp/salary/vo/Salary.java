package cn.edu.zzuli.emp.salary.vo;

public class Salary {

	private int id;
	private String eno;
	private String name;
	private double bsalary;
	private double gsalary;
	private double jbsalary;
	private double jxsalary;
	private double glsalary;

	public double getJxsalary() {
		return jxsalary;
	}

	public void setJxsalary(double jxsalary) {
		this.jxsalary = jxsalary;
	}

	public double getGlsalary() {
		return glsalary;
	}

	public void setGlsalary(double glsalary) {
		this.glsalary = glsalary;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getEno() {
		return eno;
	}

	public void setEno(String eno) {
		this.eno = eno;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getBsalary() {
		return bsalary;
	}

	public void setBsalary(double bsalary) {
		this.bsalary = bsalary;
	}

	public double getGsalary() {
		return gsalary;
	}

	public void setGsalary(double gsalary) {
		this.gsalary = gsalary;
	}

	public double getJbsalary() {
		return jbsalary;
	}

	public void setJbsalary(double jbsalary) {
		this.jbsalary = jbsalary;
	}

}
